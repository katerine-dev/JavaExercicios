/*
Faça um algoritmo que imprima a metade de cada número de 10 a 20.
*/

public class Exercicio4 {
    public static void main(String[] args) {

        double numero = 10;

        while (numero <= 20) {
            System.out.println(numero / 2);
            numero++;
        }

    }
}

