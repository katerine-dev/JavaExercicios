/*
Escreva um algoritmo que imprima a sequência do número 100 ao 200.
*/

public class Exercicio1 {
    public static void main(String[] args) {

        int numero = 100;

        while (numero <= 200) {
            System.out.print(numero + " ");
            numero++;
        }
    }
    
}
